     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dataJadi = $request->validate([
            'email' => 'required|email:rfc,dns|max:255|unique:users',
            'namaDepan' => 'required|max:255',
            'namaBelakang' => 'max:255',
            'tanggalLahir' => 'required|date',
            'jenisKelamin' => 'required',
            'password' => 'required|max:255|min:5'
        ]);

        $dataJadi['password'] = Hash::make($dataJadi['password']);

        User::create($dataJadi);

        return redirect('/login')->with('success', '<i class="success fa-solid fa-circle-check"></i>');
    }

